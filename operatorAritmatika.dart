void main() {
  print('Operator Aritmatika');
  var a = 5;
  var b = 5;
  
  var c = a+b;
  print('Penjumlahan dari $a + $b');
  print('Hasil nya adalah : $c');
  
  var d = a-b;
  print('Pengurangan dari $a - $b');
  print('Hasil nya adalah : $d');
  
  var e = a/b;
  print('Pembagian dari $a / $b');
  print('Hasil nya adalah : $e');
  
  var f = a*b;
  print('Perkalian dari $a x $b');
  print('Hasil nya adalah : $f');
  
  var g = a%b;
  print('Modulus dari $a % $b');
  print('Hasil nya adalah : $g');
  
}
